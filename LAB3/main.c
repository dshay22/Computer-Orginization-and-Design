int SumOfDifference(int* f, int* w, int* aSize, int frameStartIndex){
    int sumOfDiff = 0;
    for(int i = 0; i <  aSize[2] * aSize[3]; ++i) {
        sumOfDiff += abs(w[i] - f[i + frameStartIndex]);
        if((i & (aSize[3] - 1)) == (aSize[3] - 1)){
            frameStartIndex += aSize[1] - aSize[3];
        }
    }

    return sumOfDiff;
}

void everything(int* corrdArray, int *frame, int *window, int* aSize){
    int tempDiff, minDiff;
    minDiff = SumOfDifference(frame, window, aSize, 0);
    corrdArray[0] = 0;
    corrdArray[1] = 0;
  
    int col = 0, row = 0;
    int dirCol = 1;

    for(int i = 1; i <= ((aSize[0] - (aSize[2] - 1)) * (aSize[1] - (aSize[3] - 1))); ++i){
        if((row == 0) && (dirCol > 0) && ((col) != (aSize[1] - aSize[3]))){ // at top
            col += 1;
            dirCol = -1;
        }
        else if ((col == 0) && (dirCol < 0) && ((row) != (aSize[0] - aSize[2]))){ //at left
            row += 1;
            dirCol = 1;
        }
        else if ((col == (aSize[1] - aSize[3])) && (dirCol > 0) && ((row) != (aSize[0] - aSize[2]))){ //at right
            row += 1;
            dirCol = -1;
        }
        else if ((row == (aSize[0] - aSize[2])) && (dirCol < 0) && ((col) != (aSize[1] - aSize[3]))){ //at bottom 
            col += 1;
            dirCol = 1;
        }
        else if (dirCol < 0){ //middle going down left
            row += 1;
            col -= 1;
            dirCol = -1;
        }
        else if (dirCol > 0){ //middle going up right
            row -= 1;
            col += 1;
            dirCol = +1;
        }

        tempDiff = SumOfDifference(frame, window, aSize, row * aSize[1] + col);
        if(tempDiff < minDiff){
            minDiff = tempDiff;
            corrdArray[0] = col;
            corrdArray[1] = row;
        }
    }
}

